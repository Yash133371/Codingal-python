a = 10
b = 5

print("a =", bin(a))
print("b =", bin(b))

print("a & b =", bin(a & b))

print("a | b =", bin(a | b))

print("~a =", bin(~a))
print("~b =", bin(~b))

print("a ^ b =", bin(a ^ b))

print("a >> 1 =", bin(a >> 1))
print("b >> 1 =", bin(b >> 1))

print("a << 1 =", bin(a << 1))
print("b << 1 =", bin(b << 1))